# Programme pour parcourir les nombres de 0 à 20 et afficher un nombre sur deux dans le terminal

# Boucle for pour parcourir les nombres de 0 à 20 inclus
for nombre in range(0, 21, 2):  # Utiliser un pas de 2 pour prendre un nombre sur deux
    # Afficher chaque nombre dans le terminal
    print(nombre)